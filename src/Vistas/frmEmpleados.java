    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtCod_empleado.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de Eliminar el empleado?", "Confirmar", 2);
            if (confirmacion == 0) {
                BD_empleado func = new BD_empleado();
                empleado dts = new empleado();
                dts.setCod_empleado(Integer.parseInt(txtCod_empleado.getText()));
                func.eliminar(dts.getCod_empleado());
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

