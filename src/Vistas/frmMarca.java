    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Validacion de que se deben llenar los regitros principales

        if (txtNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el Nombre");
            txtNombre.requestFocus();
            return;
        }

        //Almacenamiento de los datos 
        //se crea un cliente nuevo
        marca ca = new marca();
        ca.setNombre_marca(txtNombre.getText());
        //se crea la clase de conexion a la base de datos
        BD_marca bd = new BD_marca();

        //Para guardar un nuevo cliente
        if (accion.equals("guardar")) {
            if (bd.insertar(ca)) {
                JOptionPane.showMessageDialog(rootPane, "La marca fue registrada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        } //para editar un cliente existente
        else if (accion.equals("editar")) {
            ca.setId_marca(Integer.parseInt(txtId_Marca.getText()));

            if (bd.editar(ca)) {
                JOptionPane.showMessageDialog(rootPane, "La marca fue editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

