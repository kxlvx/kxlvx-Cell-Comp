    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtId_Marca.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de Eliminar el La Marca?", "Confirmar", 2);
            if (confirmacion == 0) {
                BD_marca func = new BD_marca();
                marca dts = new marca();

                dts.setId_marca(Integer.parseInt(txtId_Marca.getText()));
                func.eliminar(dts.getId_marca());
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

