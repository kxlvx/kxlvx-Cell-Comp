    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Validacion de que se deben llenar los regitros principales

        if(txtNombreEmpresa.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre de la Empresa");
            txtNombreEmpresa.requestFocus();
            return;
        }

        if(txtRepresentante.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del representante");
            txtRepresentante.requestFocus();
            return;
        }

        /**
        if(txtDireccion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la direccion");
            txtDireccion.requestFocus();
            return;
        }
        * **/


        if((txtDireccion.getText().length() == 0)) {
            //JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la cedula o RUC");
            txtDireccion.setText("N/D");
            return;
        }
        
        if((txtCiudad.getText().length() == 0)) {
            //JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la cedula o RUC");
            txtCiudad.setText("N/D");
            return;
        }


        //Almacenamiento de los datos

        //se crea un cliente nuevo
        proveeedor c1 = new proveeedor();
        //se crea la clase de conexion a la base de datos
        BD_proveedor bd = new BD_proveedor();

        //llenado de datos
        c1.setNombre_empresa_pv(txtNombreEmpresa.getText());
        c1.setDireccion_pv(txtDireccion.getText());
        c1.setRepresentante(txtRepresentante.getText());
        c1.setCiudad(txtCiudad.getText());
        //Para guardar un nuevo cliente
        if(accion.equals("guardar")) {
            if(bd.insertar(c1)) {
                JOptionPane.showMessageDialog(rootPane, "El PROVEEDOR fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }

        //para editar un cliente existente
        else if(accion.equals("editar")) {
            c1.setCod_proveedor(Integer.parseInt(txtCod_proveedor.getText()));

            if(bd.editar(c1)) {
                JOptionPane.showMessageDialog(rootPane, "El proveedor fue editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

