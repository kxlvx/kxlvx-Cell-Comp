    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(!txtCod_proveedor.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de Eliminar el proveedor?", "Confirmar", 2);
            if(confirmacion == 0) {
                BD_proveedor func = new BD_proveedor();
                proveeedor dts = new proveeedor();

                dts.setCod_proveedor(Integer.parseInt(txtCod_proveedor.getText()));
                func.eliminar(dts.getCod_proveedor());
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

