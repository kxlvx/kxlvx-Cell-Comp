    void validarEmail() {
        if(!Validaciones.esEmail(txtCorreo)) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un correo valido");
            txtCorreo.setText("");
            txtCorreo.requestFocus();
            return;
        }
    }
    
    void validarTelefono() {
        if(!Validaciones.esTelefono(txtTelefono)) {
            JOptionPane.showConfirmDialog(rootPane, "El telefono debe contener numeros y debe tener un minimo de 7 a 13 digitos");
            txtTelefono.setText("");
            txtTelefono.requestFocus();
            return;
        }
    }
    
    void validadCedula() {
        if(!Validaciones.esCedula(txtCedula)) {
            JOptionPane.showConfirmDialog(rootPane, "La cedula debe contener 10 digitos, si es RUC debe contener 13");
            txtCedula.setText("");
            txtCedula.requestFocus();
            return;
        }
    }
    
    void mostrar(String buscar) {
        try {
            DefaultTableModel modelo;
            
            BD_cliente func = new BD_cliente();
            modelo = func.mostrar(buscar);
            
            tablalistado.setModel(modelo);
            lbltotalregistros.setText("" + ((func.totalregistros) + 3));
            //lbltotalregistros.setText("Total Registros " + Integer.toString(func.totalregistros));
        } 
        catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Error al acceder a los datos desde la base de datos");
        }
    }
    
