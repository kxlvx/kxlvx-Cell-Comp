    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Validacion de que se deben llenar los regitros principales

        if(txtCedula.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la cedula o RUC");
            txtCedula.requestFocus();
            return;
        }

        if(txtNombres.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar los nombres del cliente");
            txtNombres.requestFocus();
            return;
        }

        if(txtApellidos.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar Los apellidos del cliente");
            txtApellidos.requestFocus();
            return;
        }

        if(txtTelefono.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el numero de telefono o celular del cliente");
            txtTelefono.requestFocus();
            return;
        }

        if((txtDireccion.getText().length() == 0)) {
            //JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la cedula o RUC");
            txtDireccion.setText("N/D");
            return;
        }

        if(txtEmpresa.getText().length() == 0) {
            //JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la cedula o RUC");
            txtEmpresa.setText("N/D");
            return;
        }

        if(txtDirTrabajo.getText().length() == 0) {
            //JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la cedula o RUC");
            txtDirTrabajo.setText("N/D");
            return;
        }

        if(txtCorreo.getText().length() == 0) {
            //JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la cedula o RUC");
            txtCorreo.setText("N/D");
            return;
        } else {
            if(!(txtCorreo.getText().equals("") || txtCorreo.getText().equals("N/D"))) {
                validarEmail();
            }
        }

        //Almacenamiento de los datos

        //se crea un cliente nuevo
        cliente c1 = new cliente();
        //se crea la clase de conexion a la base de datos
        BD_cliente bd = new BD_cliente();

        //llenado de datos
        c1.setRuc_clie(txtCedula.getText());
        c1.setApellidos_clie(txtApellidos.getText());
        c1.setNombres_clie(txtNombres.getText());
        c1.setDireccion_clie(txtDireccion.getText());
        c1.setEmpresa(txtEmpresa.getText());
        c1.setLugar_trabajo(txtDirTrabajo.getText());
        c1.setTelefono_clie(txtTelefono.getText());
        c1.setEmail_clie(txtCorreo.getText());

        //Para guardar un nuevo cliente
        if(accion.equals("guardar")) {
            if(bd.insertar(c1)) {
                JOptionPane.showMessageDialog(rootPane, "El cliente fue registrada satisfactoriamente");
                mostrar(c1.getRuc_clie());
                inhabilitar();
                String cod;
                String ruc;
                String cliente;
                String dir;
                String telefono;
                String email;
                
                cod=tablalistado.getValueAt(0, 0).toString();
                ruc=c1.getRuc_clie();
                cliente=c1.getNombres_clie() + " " + c1.getApellidos_clie(); 
                dir=c1.getDireccion_clie();
                telefono=c1.getTelefono_clie();
                email = c1.getEmail_clie();
                frmSolicitudReparacion.txtCod_cliente.setText(cod);
                frmSolicitudReparacion.txtCI.setText(ruc);
                frmSolicitudReparacion.txtnombresApellidos.setText(cliente);
                frmSolicitudReparacion.txtDireccion.setText(dir);
                frmSolicitudReparacion.txtTelefono.setText(telefono);
                frmSolicitudReparacion.txtEmail.setText(email);
                //frmreserva.txtidcliente.setText(cod);
                //frmreserva.txtcliente.setText(valor);
                this.dispose();
            }
        }

        //para editar un cliente existente
        else if(accion.equals("editar")) {
            c1.setCod_cliente(Integer.parseInt(txtCod_cliente.getText()));

            if(bd.editar(c1)) {
                JOptionPane.showMessageDialog(rootPane, "El cliente fue editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
        
        

    }//GEN-LAST:event_btnGuardarActionPerformed

